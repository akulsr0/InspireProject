package com.akul.inspire;

public class User
{

    public String fullname, email, username, phonenumber;

    public User()
    {

    }

    public User(String fullname, String email, String username, String phonenumber) {
        this.fullname = fullname;
        this.email = email;
        this.username = username;
        this.phonenumber = phonenumber;
    }
}
